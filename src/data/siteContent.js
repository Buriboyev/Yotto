const branchesAll = ["jorabayeva", "iceberg"];

const createItem = ({
  id,
  name,
  category,
  price,
  priceLabel,
  image,
  branches = ["jorabayeva"],
  description = "",
}) => ({
  id,
  name,
  category,
  price,
  priceLabel: priceLabel ?? `${price.toLocaleString("ru-RU")} UZS`,
  image,
  branches,
  description,
});

const makeWeightedItems = ({
  idPrefix,
  name,
  category,
  image,
  branches = branchesAll,
  options,
  description = "",
}) =>
  options.map(([label, price]) =>
    createItem({
      id: `${idPrefix}-${label.replace(/[^a-z0-9]+/gi, "-").toLowerCase()}`,
      name: `${name} ${label}`,
      category,
      price,
      image,
      branches,
      description,
    }),
  );

const makePizzaItems = ({ idPrefix, name, description, prices }) =>
  [
    ["25 cm", prices[0]],
    ["30 cm", prices[1]],
    ["35 cm", prices[2]],
  ].map(([size, price]) =>
    createItem({
      id: `${idPrefix}-${size.replace(/\s+/g, "-").toLowerCase()}`,
      name: `${name} ${size}`,
      category: "pizza",
      price,
      image: images.pizza,
      branches: branchesAll,
      description,
    }),
  );


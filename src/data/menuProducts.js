import { images, menuItems } from "./siteContent";

const itemMap = Object.fromEntries(menuItems.map((item) => [item.id, item]));

const getItem = (id) => {
  const item = itemMap[id];

  if (!item) {
    throw new Error(`Unknown menu item: ${id}`);
  }

  return item;
};

const dedupe = (values) => [...new Set(values)];

const getBranches = (sourceIds) =>
  dedupe(sourceIds.flatMap((sourceId) => getItem(sourceId).branches));

const makeOption = ({
  id,
  sourceItemId,
  label,
  price,
  priceLabel,
  description,
}) => {
  const source = getItem(sourceItemId);

  return {
    id,
    sourceItemId,
    label,
    price: price ?? source.price,
    priceLabel: priceLabel ?? source.priceLabel,
    description: description ?? source.description ?? "",
  };
};

const singleProduct = ({
  id,
  name,
  category,
  image,
  description = "",
  itemId,
}) => ({
  id,
  name,
  category,
  image,
  description,
  branches: getItem(itemId).branches,
  options: [
    makeOption({
      id: itemId,
      sourceItemId: itemId,
      label: name,
    }),
  ],
});

const variantProduct = ({
  id,
  name,
  category,
  image,
  description = "",
  optionLabel,
  optionDisplay = "select",
  options,
}) => ({
  id,
  name,
  category,
  image,
  description,
  optionLabel,
  optionDisplay,
  branches: getBranches(options.map((option) => option.sourceItemId)),
  options: options.map((option) => makeOption(option)),
});

